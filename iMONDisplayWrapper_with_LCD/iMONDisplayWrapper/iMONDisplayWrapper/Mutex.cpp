//------------------------------------------------------------------------------
#include "Mutex.h"
//------------------------------------------------------------------------------
Mutex::Mutex()
{
  m_hMutex = CreateMutex(NULL, FALSE, NULL);
}
//------------------------------------------------------------------------------
Mutex::~Mutex()
{
  if (m_hMutex)
    CloseHandle(m_hMutex);
}
//------------------------------------------------------------------------------
void Mutex::Request() const
{
  WaitForSingleObject(m_hMutex, INFINITE);
}
//------------------------------------------------------------------------------
void Mutex::Release() const
{
  ReleaseMutex(m_hMutex);
}
//------------------------------------------------------------------------------
