//------------------------------------------------------------------------------
#include "Event.h"
//------------------------------------------------------------------------------
Event::Event()
{
  m_hEvent = CreateEvent(NULL, TRUE, FALSE, NULL);
}
//------------------------------------------------------------------------------
Event::~Event()
{
  if (m_hEvent)
    CloseHandle(m_hEvent);
}
//------------------------------------------------------------------------------
void Event::Signal() const
{
  SetEvent(m_hEvent);
}
//------------------------------------------------------------------------------
void Event::Reset() const
{
  ResetEvent(m_hEvent);
}
//------------------------------------------------------------------------------
void Event::Await() const
{
  WaitForSingleObject(m_hEvent, INFINITE);
}
//------------------------------------------------------------------------------
